int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
	{
		return (0);
	}
	i = 1;
	while (i * i <= nb)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}
